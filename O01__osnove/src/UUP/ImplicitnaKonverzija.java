package UUP;

public class ImplicitnaKonverzija {

	public static void main(String[] args) {
		//Napisati kod koji prikazuje implicitnu konverziju, tj automatsko pretvaranje sa užeg na širi tip podataka (engl. Widening Casting)

		int i = 10; 
		double d = i; 

		System.out.println("i " + i); 
		System.out.println("d " + d);
	}

}
